package com.percobaan1;

class Mahasiswa {

	private int Nim;
	private String Nama, Jurusan;

	public int getNim() {
		return Nim;
	}

	public void setNim(int nim) {
		Nim = nim;
	}

	public String getNama() {
		return Nama;
	}

	public void setNama(String nama) {
		Nama = nama;
	}

	public String getJurusan() {
		return Jurusan;
	}

	public void setJurusan(String jurusan) {
		Jurusan = jurusan;
	}
}