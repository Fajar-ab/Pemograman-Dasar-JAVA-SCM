/*
	Tugas 1 - program untuk menampilkan bilangan ganjil dari 100 - 186
*/

package tugas1;

public class Main {
    
    public static void main(String[] args) {

		System.out.println("\nMENAMPILKAN BILANGAN GANJIL 100 - 186");
		System.out.println("--------------------------------------");

		for (int i = 101; i < 186; i++) {
			System.out.println(i);
			i += 1;
		}
	}
}